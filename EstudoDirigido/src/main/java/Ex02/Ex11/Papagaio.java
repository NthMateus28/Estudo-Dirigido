package Ex02.Ex11;


public class Papagaio {
    String corPena;
    String raça;

    public Papagaio(String corPena, String raça) {
        this.corPena = corPena;
        this.raça = raça;
    }

    public Papagaio() {
        }
    
    public String getCorPena() {
        return corPena;
    }

    public void setCorPena(String corPena) {
        this.corPena = corPena;
    }

    public String getRaça() {
        return raça;
    }

    public void setRaça(String raça) {
        this.raça = raça;
    }

    
    
    
}
