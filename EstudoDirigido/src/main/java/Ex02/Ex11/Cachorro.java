
package Ex02.Ex11;


public class Cachorro {
    String corPelo;
    String raça;

    public Cachorro() {    
    }

    public Cachorro(String corPelo, String raça) {
        this.corPelo = corPelo;
        this.raça = raça;
    }

    public String getCorPelo() {
        return corPelo;
    }

    public void setCorPelo(String corPelo) {
        this.corPelo = corPelo;
    }

    public String getRaça() {
        return raça;
    }

    public void setRaça(String raça) {
        this.raça = raça;
    }
    
    
}
